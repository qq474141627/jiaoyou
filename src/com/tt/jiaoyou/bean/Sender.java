package com.tt.jiaoyou.bean;

public class Sender {
	
	private String text;
	private String sender_nick;
	private String sender;
	private String sender_icon;
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getSender_nick() {
		return sender_nick;
	}
	public void setSender_nick(String sender_nick) {
		this.sender_nick = sender_nick;
	}
	public String getSender() {
		return sender;
	}
	public void setSender(String sender) {
		this.sender = sender;
	}
	public String getSender_icon() {
		return sender_icon;
	}
	public void setSender_icon(String sender_icon) {
		this.sender_icon = sender_icon;
	}

	
}
